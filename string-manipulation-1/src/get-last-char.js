// function: getLastChar
// argument: string
// var lastCharacterOfString
//
// storage for last character
// get first character of string
// store it in last character
// produce last character

function getLastChar(string) {
  var lastCharacterOfString;
  lastCharacterOfString = string[string.length - 1];
  return lastCharacterOfString;
}
