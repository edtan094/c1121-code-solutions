/* exported last */
// function: last
// argument: array
// return: last element of array

// create storage for where you will be putting the last element of this array
// find out the length of this array and use this get the last element of the array
// put this element into the storage you just created
// produce this storage
function last(array) {
  var newArray;
  newArray = array[array.length - 1];
  return newArray;
}
